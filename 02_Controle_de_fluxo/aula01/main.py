# if, elif and else

velocidade = 110

if velocidade >= 110:
    print('Velocidade acima do permitido.')
    print('Favor, reduzir sua velocidade.')
elif velocidade < 60:
    print('Velocidade abaixo do permitido')
    print('Favor, dirigir acima de 80km/h')
else:
    print('Velocidade Permitida. Continue assim.')
    