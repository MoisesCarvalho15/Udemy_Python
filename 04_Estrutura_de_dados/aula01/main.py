# Manipulando listas

# Armazenando vários dados em uma variável
# index       0          1         2        3        4
frutas = ['Abacate', 'Abacaxi', 'Amora', 'Banana', 'Maça']

# Imprimindo toda a lista
print(frutas)

# Pegando um item da listas
print(f'Primeiro item da lista: {frutas[0]}')

# Trocando o nome de um item da lista
frutas[2] = 'Caqui'
print(frutas)
